#include "libftprintf.h"

void	updater(char *str)
{
	while (*str == '%')
		str++;
	while (*str == 32)
		str++;
	while (*str == "c" || *str == "s" || *str == "p" || \
	*str == "d" || *str == "i" || *str == "u" || \
	*str == "x" || *str == "X" || *str == '%')
		str++;
}
